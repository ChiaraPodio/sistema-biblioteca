    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (txtIDLibro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete el campo.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            Libro libro = GestionLibros.buscarLibroPorId(Integer.parseInt(txtIDLibro.getText()));
            if (libro != null) {
                JOptionPane.showMessageDialog(this, "La cantidad disponible es " + libro.getCantidadDisponible(), "Cantidad de libros", JOptionPane.INFORMATION_MESSAGE);

                GestionOpciones GestionOpcion1 = new GestionOpciones();
                GestionOpcion1.setVisible(true);
                GestionOpcion1.setLocationRelativeTo(null);
                this.dispose();
            } else {
                lblLibroNoEncontrado.setText("Libro No Encontrado");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El campo debe contener solo n√∫meros.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

