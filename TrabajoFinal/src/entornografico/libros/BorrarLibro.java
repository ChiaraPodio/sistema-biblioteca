    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (txtIDLibro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete el campo.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            if (GestionLibros.borrarLibro(Integer.parseInt(txtIDLibro.getText()))) {
                JOptionPane.showMessageDialog(this, "El libro fue borrado.", "Borrado exitoso", JOptionPane.INFORMATION_MESSAGE);

                GestionOpciones GestionOpcion1 = new GestionOpciones();
                GestionOpcion1.setVisible(true);
                GestionOpcion1.setLocationRelativeTo(null);
                this.dispose();
                
            } else {
                lblMensajeError.setText("El libro no existe");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El campo debe contener solo n√∫meros.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed


